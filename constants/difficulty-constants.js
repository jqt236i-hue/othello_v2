function getDialogueForOutcome(level, outcome) {
    const levelDialogue = CPU_LEVEL_DIALOGUES[level] || CPU_LEVEL_DIALOGUES[1];
    const entry = levelDialogue[outcome];
    
    if (Array.isArray(entry)) {
        return entry[Math.floor(Math.random() * entry.length)];
    }
    return entry || '';
}
